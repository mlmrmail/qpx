    let sliceSummary = {};
    let sliceArray = [];
    // let solutions = [];

    _.forEach(response.trips.tripOption, (tripOption)=> {
        _.forEach(tripOption.pricing, (pricing) => {
            _.forEach(pricing.fare, (fare)=> {
                sliceSummary.origin = fare.origin;
                sliceSummary.destination = fare.destination;
                sliceArray.push(sliceSummary);
                sliceSummary={};
            } );
        });
        // solutions.push(sliceArray);
        // sliceArray = [];
        return false;
    });

    return sliceArray;
